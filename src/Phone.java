public class Phone {
    int storage;
    int screenSize;
    String processor;
    int Price;
    String chargerType;

    public Phone() {

    }

    public Phone(int storage, int screenSize, String processor, int Price, String chargerType) {
        this.storage = storage;
        this.screenSize = screenSize;
        this.processor = processor;
        this.Price = Price;
        this.chargerType = chargerType;
    }
}
